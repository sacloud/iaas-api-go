package main

import (
	"fmt"
	"log"
	"sort"
	"strings"

	"github.com/sacloud/iaas-api-go/internal/tools/fieldmanifest"
)

// excludedFields は fieldmanifest allowlist により TypeSpec 出力から除外された
// (TypeSpec モデル名, TypeSpec フィールド名) を順に記録する。
// gen-typespec 終了時に docs/typespec/excluded-fields.md に書き出す。
var excludedFields [][2]string

// recordExcludedField は (modelName, fieldName) を除外ログに記録する。
// 同一 (model, field) が複数回現れても単純に append する。重複は report 出力時に除く。
func recordExcludedField(modelName, fieldName string) {
	excludedFields = append(excludedFields, [2]string{modelName, fieldName})
}

// writeExcludedFieldsReport は excludedFields と fieldmanifest.Manifest の登録状況
// を突き合わせ、docs/typespec/excluded-fields.md を上書き出力する。
// fieldmanifest に登録されているモデルのみレポート対象とする (未登録モデルは
// pass-through なので除外フィールドが存在し得ない)。
func writeExcludedFieldsReport() {
	// 同一 (model, field) の重複を除く
	type key struct{ m, f string }
	seen := map[key]bool{}
	byModel := map[string][]string{}
	for _, rec := range excludedFields {
		k := key{rec[0], rec[1]}
		if seen[k] {
			continue
		}
		seen[k] = true
		byModel[rec[0]] = append(byModel[rec[0]], rec[1])
	}

	var modelNames []string
	for m := range byModel {
		modelNames = append(modelNames, m)
	}
	sort.Strings(modelNames)

	var b strings.Builder
	b.WriteString("<!-- generated by 'github.com/sacloud/iaas-api-go/internal/tools/gen-typespec'; DO NOT EDIT -->\n")
	b.WriteString("# TypeSpec 出力から除外されたフィールド一覧\n\n")
	b.WriteString("このファイルは `go run ./internal/tools/gen-typespec` により自動生成される。手で編集しないこと。\n\n")
	b.WriteString("`internal/tools/fieldmanifest/manifest.go` の allowlist に登録されたモデルのうち、\n")
	b.WriteString("allowlist に含まれず TypeSpec / OpenAPI / v2 Go client に emit されなかったフィールドをここに列挙する。\n\n")
	b.WriteString("追加が必要になったら fieldmanifest に列挙し、`go generate ./...` を走らせ直すこと。\n\n")
	b.WriteString("対象 downstream: usacloud / terraform-provider-sakuracloud (v2) / terraform-provider-sakura (v3) とそれらが参照する iaas-service-go のシンボル。\n\n")

	if len(modelNames) == 0 {
		b.WriteString("_現在、除外されたフィールドはありません。_\n")
	} else {
		for _, m := range modelNames {
			fields := byModel[m]
			sort.Strings(fields)
			b.WriteString(fmt.Sprintf("## %s\n\n", m))
			b.WriteString("| Field |\n|---|\n")
			for _, f := range fields {
				b.WriteString(fmt.Sprintf("| %s |\n", f))
			}
			b.WriteString("\n")
		}
	}

	// 登録済みモデルのうち、除外フィールドが 0 件だったもの (= allowlist が DSL 全てを
	// カバーしている) も参考情報として載せる。
	var coveredModels []string
	for m := range fieldmanifest.Manifest {
		if _, hasExcl := byModel[m]; !hasExcl {
			coveredModels = append(coveredModels, m)
		}
	}
	sort.Strings(coveredModels)
	if len(coveredModels) > 0 {
		b.WriteString("## 参考: allowlist 登録済みで除外対象フィールドが無いモデル\n\n")
		for _, m := range coveredModels {
			b.WriteString(fmt.Sprintf("- %s\n", m))
		}
		b.WriteString("\n")
	}

	writeFile(b.String(), nil, "docs/typespec/excluded-fields.md", nil)
	log.Printf("generated: docs/typespec/excluded-fields.md\n")
}
