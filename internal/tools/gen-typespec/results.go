package main

import (
	"path/filepath"
	"text/template"

	"github.com/sacloud/iaas-api-go/internal/define"
	"github.com/sacloud/iaas-api-go/internal/dsl"
)

// resultInfo は1オペレーション分のresult情報を保持する。
type resultInfo struct {
	ResultTypeName     string
	IsResponsePlural   bool
	IsResponseSingular bool
	Results            dsl.Results
}

// resourceResults は1リソース分の全resultをまとめたテンプレートパラメータ。
type resourceResults struct {
	Results []resultInfo
}

// resultsTmpl は1リソース分の全resultを1ファイルに出力するテンプレート。
const resultsTmpl = `// generated by 'github.com/sacloud/iaas-api-go/internal/tools/gen-typespec'; DO NOT EDIT

import "@typespec/http";

namespace Sacloud.IaaS;
{{ range .Results }}
model {{ .ResultTypeName }} {
{{ if .IsResponsePlural }}
	@doc("対象リソースの総件数")
	Total: int32;

	@doc("現在のページ番号")
	From: int32;

	@doc("現在のページの件数")
	Count: int32;

	{{- range .Results }}
	@doc("検索結果")
	{{ .DestField }}: {{ .Model.Name }}[];
	{{- end }}
{{ else }}
	@doc("オペレーションが成功したかどうかを示すフラグ")
	is_ok: boolean;

	{{- range .Results }}
	@doc("{{ .DestField }}")
	{{ .DestField }}: {{ .Model.Name }};
	{{- end }}
{{ end }}
}
{{ end }}`

func generateResults() {
	for _, api := range define.APIs {
		var results []resultInfo

		for _, op := range api.Operations {
			if !op.UseWrappedResult {
				continue
			}

			results = append(results, resultInfo{
				ResultTypeName:     op.ResultTypeName(),
				IsResponsePlural:   op.IsResponsePlural(),
				IsResponseSingular: op.IsResponseSingular(),
				Results:            op.Results,
			})
		}

		if len(results) == 0 {
			// resultがなければスキップ
			continue
		}

		outFile := filepath.Join(resourcesDir, api.FileSafeName(), "results.tsp")
		writeFile(resultsTmpl, resourceResults{Results: results}, outFile, template.FuncMap{
			"lowerFirst": lowerFirst,
		})
	}
}
